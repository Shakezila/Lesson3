
public class Circle {
	private int R;
	private int x;
	private int y;
	private double P;
	private double S;
	
	public Circle (int R,int x) {
		this.R = R;
		this.x = x;
	}
	
	public int getR() {
		return R;
	}
	public void setR(int r) {
		R = r;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public double getP() {
		return P;
	}
	public void setP(double p) {
		P = p;
	}
	public double getS() {
		return S;
	}
	public void setS(double s) {
		S = s;
	}
}
